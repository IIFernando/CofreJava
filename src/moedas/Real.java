package moedas;

public class Real {

	public double valor;

	public void saldo() {
		this.valor += valor;
	}

	public void adicionar(double quantity) {
		this.valor += quantity;
	}

	public void remover(double quantity) {
		this.valor -= quantity;
	}
	
    public double getValorReal() {
        return valor;
    }

}
